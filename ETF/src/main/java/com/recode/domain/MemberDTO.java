package com.recode.domain;

public class MemberDTO {
	private String id;
	private String pass;
	private String kaid;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getKaid() {
		return kaid;
	}
	public void setKaid(String kaid) {
		this.kaid = kaid;
	}
	
}
